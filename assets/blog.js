/* Boostyou.ai — NEWS page blog feed.

   Posts are plain .txt files in the /posts folder of the GitHub repo:
     posts/YYYY-MM-DD-some-slug.txt
   First line = title, rest = body (blank line = new paragraph).

   The list of posts comes from the GitHub API (drop a file in the folder
   and it appears); posts/index.json is the fallback when the API is
   rate-limited or unreachable. File contents are fetched same-origin.
   Posts render in batches; an IntersectionObserver sentinel loads the
   next batch when scrolled near (infinite scroll). */

(function () {
  var REPO = 'Nutrichi/boostyou';
  var POSTS_PATH = 'posts';
  var POSTS_BASE = '../posts/';
  var BATCH_SIZE = 4;

  var NAME_RE = /^(\d{4})-(\d{2})-(\d{2})-(.+)\.txt$/;
  var MONTHS = ['January', 'February', 'March', 'April', 'May', 'June',
    'July', 'August', 'September', 'October', 'November', 'December'];

  var feed, sentinel, status;
  var queue = [];
  var loading = false;

  function esc(s) {
    return s.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
  }

  function linkify(s) {
    return s.replace(/https?:\/\/[^\s<]+/g, function (url) {
      return '<a href="' + url + '" target="_blank" rel="noreferrer">' + url + '</a>';
    });
  }

  function formatDate(name) {
    var m = NAME_RE.exec(name);
    if (!m) return '';
    return parseInt(m[3], 10) + ' ' + MONTHS[parseInt(m[2], 10) - 1] + ' ' + m[1];
  }

  function listFromApi() {
    return fetch('https://api.github.com/repos/' + REPO + '/contents/' + POSTS_PATH)
      .then(function (r) {
        if (!r.ok) throw new Error('GitHub API ' + r.status);
        return r.json();
      })
      .then(function (entries) {
        return entries.map(function (e) { return e.name; });
      });
  }

  function listFromManifest() {
    return fetch(POSTS_BASE + 'index.json')
      .then(function (r) {
        if (!r.ok) throw new Error('manifest ' + r.status);
        return r.json();
      })
      .then(function (data) { return data.posts; });
  }

  function renderPost(name, text) {
    var lines = text.replace(/\r/g, '').split('\n');
    var title = '';
    while (lines.length && !title) title = lines.shift().trim();
    var paragraphs = lines.join('\n').trim().split(/\n\s*\n/);

    // permalink to the static SEO twin generated by tools/post_pages.py
    var permalink = 'news-' + name.replace(/\.txt$/, '.html');
    var article = document.createElement('article');
    article.className = 'blog-post';
    article.innerHTML =
      '<div class="blog-date">' + formatDate(name) + '</div>' +
      '<h2><a href="' + permalink + '" style="color:inherit;text-decoration:none;">' + esc(title) + '</a></h2>' +
      paragraphs.map(function (p) {
        return '<p>' + linkify(esc(p.trim())).replace(/\n/g, '<br>') + '</p>';
      }).join('');
    feed.appendChild(article);
  }

  function loadBatch() {
    if (loading || !queue.length) return;
    loading = true;
    status.textContent = 'Loading…';
    var batch = queue.splice(0, BATCH_SIZE);

    Promise.all(batch.map(function (name) {
      return fetch(POSTS_BASE + encodeURIComponent(name))
        .then(function (r) {
          if (!r.ok) throw new Error(name + ' ' + r.status);
          return r.text();
        })
        .then(function (text) { return { name: name, text: text }; })
        .catch(function () { return null; });
    })).then(function (posts) {
      posts.forEach(function (p) {
        if (p) renderPost(p.name, p.text);
      });
      loading = false;
      if (queue.length) {
        status.textContent = '';
      } else {
        status.textContent = 'You’re all caught up ✔';
        sentinel.remove();
      }
    });
  }

  function start(names) {
    queue = names
      .filter(function (n) { return NAME_RE.test(n); })
      .sort()
      .reverse();
    if (!queue.length) {
      status.textContent = 'No news posts yet — check back soon!';
      return;
    }
    loadBatch();
    new IntersectionObserver(function (entries) {
      if (entries[0].isIntersecting) loadBatch();
    }, { rootMargin: '600px' }).observe(sentinel);
  }

  document.addEventListener('DOMContentLoaded', function () {
    feed = document.getElementById('blogFeed');
    sentinel = document.getElementById('blogSentinel');
    status = document.getElementById('blogStatus');
    if (!feed) return;

    listFromApi()
      .catch(listFromManifest)
      .then(start)
      .catch(function () {
        status.textContent = 'Could not load news posts right now — please try again later.';
      });
  });
})();
