/* Boostyou.ai — shared navigation behavior.
   Markup is generated by tools/stamp_nav.py. */

function toggleMobileMenu() {
  var menu = document.getElementById('navMenu');
  var toggle = document.querySelector('.mobile-toggle');
  if (!menu) return;
  var open = menu.classList.toggle('mobile-open');
  if (toggle) toggle.setAttribute('aria-expanded', open ? 'true' : 'false');
}

document.addEventListener('DOMContentLoaded', function () {
  // Mobile accordion: the caret button expands a section's submenu,
  // the section label itself still navigates to the hub page.
  document.querySelectorAll('.submenu-toggle').forEach(function (btn) {
    btn.addEventListener('click', function () {
      var item = btn.closest('.nav-item');
      if (!item) return;
      var open = item.classList.toggle('open');
      btn.setAttribute('aria-expanded', open ? 'true' : 'false');
    });
  });

  // Reset menu state when resizing back to desktop
  window.addEventListener('resize', function () {
    if (window.innerWidth > 768) {
      var menu = document.getElementById('navMenu');
      if (menu) menu.classList.remove('mobile-open');
      document.querySelectorAll('.nav-item.open').forEach(function (item) {
        item.classList.remove('open');
      });
    }
  });
});
